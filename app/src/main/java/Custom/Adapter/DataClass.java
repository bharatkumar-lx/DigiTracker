package Custom.Adapter;

public class DataClass {
    private String timeUse;
    private String image;

    public DataClass(String timeUse, String image) {
        this.timeUse = timeUse;
        this.image = image;
    }

    public String getTimeUse() {
        return timeUse;
    }

    public String getImage() {
        return image;
    }

}
